
public class AreaBean {
	private String regionName;//地区
	private String gdp;//国民生产总值GDP(亿元)
	private String publicRevenue;//地方公共财政收入(亿元)
	private String publicExpenditure;//地方公共财政支出(亿元)
	private String permanentPopulation;//常住人口(万人)
	private String registeredPopulation;//户籍人口(万人) 
	private String pppProjectsNum;//PPP项目数(个)
	public String getRegionName() {
		return regionName;
	}
	public void setRegionName(String regionName) {
		this.regionName = regionName;
	}
	public String getGdp() {
		return gdp;
	}
	public void setGdp(String gdp) {
		this.gdp = gdp;
	}
	public String getPublicRevenue() {
		return publicRevenue;
	}
	public void setPublicRevenue(String publicRevenue) {
		this.publicRevenue = publicRevenue;
	}
	public String getPublicExpenditure() {
		return publicExpenditure;
	}
	public void setPublicExpenditure(String publicExpenditure) {
		this.publicExpenditure = publicExpenditure;
	}
	public String getPermanentPopulation() {
		return permanentPopulation;
	}
	public void setPermanentPopulation(String permanentPopulation) {
		this.permanentPopulation = permanentPopulation;
	}
	public String getRegisteredPopulation() {
		return registeredPopulation;
	}
	public void setRegisteredPopulation(String registeredPopulation) {
		this.registeredPopulation = registeredPopulation;
	}
	public String getPppProjectsNum() {
		return pppProjectsNum;
	}
	public void setPppProjectsNum(String pppProjectsNum) {
		this.pppProjectsNum = pppProjectsNum;
	}
	
	
}
