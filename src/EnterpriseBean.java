
public class EnterpriseBean {
	private String name;//企业名字
	private String nature;//企业性质
	private String isListed;//是否上市公司
	private String projectCount;//中标项目数
	private String projectScale;//中标投资规模
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getNature() {
		return nature;
	}
	public void setNature(String nature) {
		this.nature = nature;
	}
	public String getIsListed() {
		return isListed;
	}
	public void setIsListed(String isListed) {
		this.isListed = isListed;
	}
	public String getProjectCount() {
		return projectCount;
	}
	public void setProjectCount(String projectCount) {
		this.projectCount = projectCount;
	}
	public String getProjectScale() {
		return projectScale;
	}
	public void setProjectScale(String projectScale) {
		this.projectScale = projectScale;
	}
	
	
}
